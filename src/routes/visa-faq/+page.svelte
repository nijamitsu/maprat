				a valid passport or ID.
			</p>
			<br />
			<h2 id="what-is-visa-on-arrival">What is visa on arrival?</h2>
			<p>
				Visa on arrival allows you to obtain a visa when you arrive in a country, rather than
				applying for one beforehand. You will need to meet certain requirements, such as having a
				valid passport, proof of return travel, and sometimes a fee, depending on the country.
			</p>
			<br />
			<h2 id="what-is-eta">What is an ETA?</h2>
			<p>
				An Electronic Travel Authorization (ETA) is an entry requirement for visa-exempt foreign
				nationals, linked to your passport. It is a simple online application, often processed the
				same day.
			</p>
