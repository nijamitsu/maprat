			<h2 id="what-is-tourist-visa">What is a tourist visa?</h2>
			<p>
				A tourist visa grants you permission to stay in or transit through a country for leisure
				purposes. Tourist visas specify the duration of stay, permitted activities, and areas within
				the country the individual may enter.
			</p>
			<br />
			<h2 id="what-is-business-visa">What is a business visa?</h2>
			<p>
				A business visa allows you to stay, or transit through a country for business purposes.
				Business visas are often required for attending meetings, conferences, or conducting
				business activities in a foreign country.
			</p>
			<br />
			<h2 id="what-is-student-visa">What is a student visa?</h2>
			<p>
				A student visa permits you to study in a country for educational purposes. Student visas are
				commonly required for enrolling in academic programs, including universities, colleges, or
				language schools, in a foreign country.
			</p>
			<br />
			<h2 id="what-is-visa-free">What is visa free?</h2>
			<p>
				Visa free means you can enter, stay, or transit through a country without needing a visa.
